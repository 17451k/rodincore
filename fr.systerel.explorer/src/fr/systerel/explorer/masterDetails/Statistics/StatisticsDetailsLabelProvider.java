	/* (non-Javadoc)
	 * @see org.eclipse.jface.viewers.ITableLabelProvider#getColumnText(java.lang.Object, int)
	 */
	public String getColumnText(Object element, int columnIndex) {
		if (element instanceof Statistics) {
			Statistics stats = (Statistics) element;
	        String result = "";
	        switch (columnIndex) {
	            case 0:  
	                result = stats.getParentLabel();
	                break;
	            case 1:  
	                result = result +stats.getTotal();
	                break;
	            case 2 :
	                result = result +(stats.getTotal() -stats.getManual() -stats.getUndischarged());
	                break;
	            case 3 :
	                result = result +stats.getManual();
	                break;
	            case 4 :
	                result = result +stats.getReviewed();
	                break;
	            case 5 :
	                result = result +(stats.getUndischarged() -stats.getReviewed());
	                break;
	            }
	        return result;
		}
		return null;
	}

